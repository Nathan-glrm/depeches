public class PaireEntierComparaison {
    private int entier;
    private int nbComp;

    public PaireEntierComparaison(int entier, int nbComp) {
        this.entier = entier;
        this.nbComp = nbComp;
    }

    public int getEntier() {
        return entier;
    }

    public int getNbComp() {
        return nbComp;
    }
}
